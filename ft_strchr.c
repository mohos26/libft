#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*ptr;

	ptr = (char *)s;
	while (*ptr != (char)c)
	{
		if (!*ptr)
			return (0);
		ptr++;
	}
	return (ptr);
}
